/**
 * Splice 86 web2/* sub-pages into shared/js/navigation-modern.js as:
 *   - 86 nav items (one per page) injected between WEB2_NAV_ITEMS markers
 *   - 12 sub-group entries injected between WEB2_NAV_GROUPS markers
 *
 * Idempotent — safely re-runnable. Reads from web2/modules-manifest.js.
 *
 * Run after `node scripts/web2-build-manifest.js`.
 */
const fs = require('fs');
const path = require('path');

const NAV_FILE = 'shared/js/navigation-modern.js';
const MANIFEST_FILE = 'web2/modules-manifest.js';

// Parse manifest by executing in a sandbox (handles JSON or JS-literal style after linter)
function readManifest() {
    const vm = require('vm');
    const src = fs.readFileSync(MANIFEST_FILE, 'utf8');
    const sandbox = { window: {} };
    vm.createContext(sandbox);
    vm.runInContext(src, sandbox, { filename: MANIFEST_FILE });
    if (!Array.isArray(sandbox.window.WEB2_MODULES_MANIFEST)) {
        throw new Error(`window.WEB2_MODULES_MANIFEST not an array in ${MANIFEST_FILE}`);
    }
    return sandbox.window.WEB2_MODULES_MANIFEST;
}

const modules = readManifest();
console.log(`Loaded ${modules.length} modules from manifest`);

// Generate nav items — one per page.
// pageIdentifier convention: `web2-<dir>` to avoid collision with v1 pages.
function makeNavItem(m) {
    const id = `web2-${m.dir}`;
    return `    {
        href: '../web2/${m.dir}/index.html',
        icon: ${JSON.stringify(m.icon || 'circle')},
        text: ${JSON.stringify(`[V2] ${m.title}`)},
        shortText: ${JSON.stringify(m.title.length > 20 ? m.title.slice(0, 18) + '…' : m.title)},
        pageIdentifier: ${JSON.stringify(id)},
        permissionRequired: 'web2-launcher',
        publicAccess: true,
    },`;
}

const navItemsCode = modules.map(makeNavItem).join('\n');

// Build single Web 2.0 group items list (4 originals + 86 generated)
const ORIGINAL_WEB2_GROUP_ITEMS = [
    'tpos-pancake',
    'native-orders',
    'web2-products',
    'web2-launcher',
];
const sortedModules = modules.slice().sort((a, b) => {
    // Sort by category then title (Vietnamese-aware) — gives deterministic ordering inside the flat group
    return (
        (a.category || '').localeCompare(b.category || '', 'vi') ||
        (a.title || '').localeCompare(b.title || '', 'vi')
    );
});
const allGroupIds = [...ORIGINAL_WEB2_GROUP_ITEMS, ...sortedModules.map((m) => `web2-${m.dir}`)];
const groupItemsLine = `        items: [${allGroupIds.map((id) => `'${id}'`).join(', ')}],`;

// No separate sub-groups — sub-group region is wiped clean.
const groupsCode = '';

console.log(
    `Generated ${modules.length} nav items, 1 unified Web 2.0 group with ${allGroupIds.length} items`
);

// Splice into nav file via anchor markers
function spliceBetween(src, startMarker, endMarker, replacement) {
    const startIdx = src.indexOf(startMarker);
    const endIdx = src.indexOf(endMarker);
    if (startIdx === -1 || endIdx === -1) {
        throw new Error(`Markers not found: ${startMarker} / ${endMarker}`);
    }
    const before = src.slice(0, startIdx + startMarker.length);
    const after = src.slice(endIdx);
    return `${before}\n${replacement}\n    ${after}`;
}

let nav = fs.readFileSync(NAV_FILE, 'utf8');
nav = spliceBetween(
    nav,
    '// -- WEB2_NAV_ITEMS_START -- (auto-generated by scripts/web2-build-nav.js)',
    '// -- WEB2_NAV_ITEMS_END --',
    navItemsCode
);
nav = spliceBetween(
    nav,
    '// -- WEB2_NAV_GROUPS_START -- (auto-generated by scripts/web2-build-nav.js)',
    '// -- WEB2_NAV_GROUPS_END --',
    groupsCode
);
nav = spliceBetween(
    nav,
    '// -- WEB2_GROUP_ITEMS_START -- (auto-generated by scripts/web2-build-nav.js)',
    '// -- WEB2_GROUP_ITEMS_END --',
    groupItemsLine
);

fs.writeFileSync(NAV_FILE, nav);
console.log(`Wrote ${NAV_FILE}`);

// Quick syntax check
const { execSync } = require('child_process');
try {
    execSync(`node --check ${NAV_FILE}`, { stdio: 'pipe' });
    console.log('✅ syntax OK');
} catch (e) {
    console.error('❌ syntax error:', e.stderr?.toString() || e.message);
    process.exit(2);
}

console.log('\nFlat group summary:');
console.log(`  Web 2.0 (single group): ${allGroupIds.length} items`);

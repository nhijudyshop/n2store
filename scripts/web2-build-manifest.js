/**
 * Build web2/modules-manifest.js — list of all web2/* sub-pages with metadata
 * extracted from each page's Web2Shell.bootstrap or Web2Page.mount config.
 *
 * Output: window.WEB2_MODULES_MANIFEST = [...]
 *
 * Idempotent — safe to re-run after adding new pages.
 */
const fs = require('fs');
const path = require('path');

const WEB2_DIR = 'web2';

// breadcrumb[1] → icon mapping (lucide icons)
const ICON_BY_CATEGORY = {
    'Sản phẩm': 'box',
    'Bán hàng': 'shopping-cart',
    'Mua hàng': 'shopping-bag',
    'Khách hàng': 'users',
    'Nhà cung cấp': 'truck',
    Kho: 'package',
    'Tồn kho': 'archive',
    'Tài chính': 'dollar-sign',
    'Kế toán': 'calculator',
    'Cấu hình kế toán': 'sliders',
    'Cấu hình': 'settings',
    'Báo cáo': 'bar-chart-3',
    'Kênh bán': 'globe',
    Marketing: 'megaphone',
    'Hệ thống': 'cpu',
    App: 'layout-grid',
};

function extractMeta(html) {
    // Try title from bootstrap config (json or js literal)
    const bsTitle = html.match(/["']?title["']?\s*:\s*['"]([^'"]+)['"]/);
    const bsSlug = html.match(/["']?slug["']?\s*:\s*['"]([^'"]+)['"]/);
    const breadcrumb = html.match(/["']?breadcrumb["']?\s*:\s*\[([^\]]+)\]/);
    let category = '';
    if (breadcrumb) {
        // Extract second element (index 1) — first is "App"
        const items = breadcrumb[1]
            .split(',')
            .map((s) => s.trim().replace(/^['"]|['"]$/g, ''))
            .filter(Boolean);
        category = items[1] || items[0] || '';
    }
    return {
        title: bsTitle ? bsTitle[1] : '',
        slug: bsSlug ? bsSlug[1] : '',
        category,
    };
}

const dirs = fs
    .readdirSync(WEB2_DIR)
    .filter((d) => {
        const full = path.join(WEB2_DIR, d);
        return fs.statSync(full).isDirectory() && fs.existsSync(path.join(full, 'index.html'));
    })
    .sort();

const modules = [];
for (const d of dirs) {
    const html = fs.readFileSync(path.join(WEB2_DIR, d, 'index.html'), 'utf8');
    const meta = extractMeta(html);
    if (!meta.title) {
        // skip pages without title — they're broken stubs
        continue;
    }
    modules.push({
        dir: d,
        title: meta.title,
        slug: meta.slug,
        category: meta.category || 'Khác',
        icon: ICON_BY_CATEGORY[meta.category] || 'circle',
    });
}

const out = `// #Note: Auto-generated by scripts/web2-build-manifest.js. DO NOT EDIT manually.
// Re-run script after adding/removing web2/* pages.
window.WEB2_MODULES_MANIFEST = ${JSON.stringify(modules, null, 2)};
`;

fs.writeFileSync(path.join(WEB2_DIR, 'modules-manifest.js'), out);

// Summary
const byCategory = {};
for (const m of modules) {
    byCategory[m.category] = (byCategory[m.category] || 0) + 1;
}

console.log(`Wrote ${path.join(WEB2_DIR, 'modules-manifest.js')}`);
console.log(`Total modules: ${modules.length}`);
console.log('Per category:');
for (const [c, n] of Object.entries(byCategory).sort((a, b) => b[1] - a[1])) {
    console.log(`  ${c.padEnd(30)}  ${n}`);
}
